package projects.reactiveSpanner.nodes.messages.data;

import java.util.HashMap;
import java.util.HashSet;
import java.util.Set;

import sinalgo.nodes.Node;

public class AdjacencyLists implements Data {
	private HashMap<Node, Set<Node>> blueWangLi = new HashMap<Node, Set<Node>>();
	private HashMap<Node, Set<Node>> redWangLi = new HashMap<Node, Set<Node>>();
	private HashMap<Node, Set<Node>> greenWangLi = new HashMap<Node, Set<Node>>();
	
	
	public AdjacencyLists(HashMap<Node, Set<Node>> blueWangLi, HashMap<Node, Set<Node>> redWangLi, HashMap<Node, Set<Node>> greenWangLi) {
		this.blueWangLi = blueWangLi;
		this.redWangLi = redWangLi;
		this.greenWangLi = greenWangLi;
	}


	public HashMap<Node, Set<Node>> getBlueWangLi() {
		return blueWangLi;
	}

	public HashMap<Node, Set<Node>> getRedWangLi() {
		return redWangLi;
	}

	public HashMap<Node, Set<Node>> getGreenWangLi() {
		return greenWangLi;
	}



}
