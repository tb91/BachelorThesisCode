package projects.reactiveSpanner.nodes.edges;

import sinalgo.nodes.edges.Edge;


/**
 * @author Mavs
 *
 * Interface for the weight of an edge. This could be used for routing algorithms to determine
 * and compare different types of weights of an edge.
 * 
 *
 * @param <T> type of weight (e.g. Integer, Double,..)
 * @param <U> edge type that is used for comparison
 */
public interface Weighted<T extends Number, U extends Edge> extends Comparable<U> {

	public T weight(); 
}
